export type BrazilianState = typeof BRAZILIAN_STATES[number]['value'];

/**
 * Get state label by value
 * @param value - State abbreviation (e.g., 'SP')
 * @returns State name or the value if not found
 */
export function getStateLabel(value: string | null | undefined): string {
  if (!value) return '';
  const state = BRAZILIAN_STATES.find((s) => s.value === value);
  return state?.label || value;
}
